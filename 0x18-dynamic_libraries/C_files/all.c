#include <stdio.h>

/**
 * add - Adding two int
 * @a: int 1
 * @b: int 2
 * Return: return the value of add
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtrating two int
 * @a: int 1
 * @b: int 2
 * Return: return the value of sub
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - dividing two int
 * @a: int 1
 * @b: int 2
 * Return: return the value of div
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - mul two int
 * @a: int 1
 * @b: int 2
 * Return: return the value of mul
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - mod two int
 * @a: int 1
 * @b: int 2
 * Return: return the value of mod
 */
int mod(int a, int b)
{
	return (a % b);
}
